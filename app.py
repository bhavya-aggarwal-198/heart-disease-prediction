import streamlit as st
import pandas as pd
import joblib as jb

# loading models
model = jb.load("lr_heart.pkl")
scaler = jb.load("scaler.pkl")
expected_columns = jb.load("columns.pkl")

st.title("Heart stroke prediction by Bhavya")
st.warning("""
⚠️ Medical Disclaimer

This application is an educational machine learning project created by Bhavya .

The predictions generated by this system are based on historical dataset patterns and are not intended to diagnose, treat, cure, or prevent any disease.

Do not use this application as a substitute for professional medical advice, diagnosis, or treatment. Always consult a qualified healthcare professional regarding any medical concerns.
""")
st.markdown("provide the following details")
age = st.slider("Age" , 18 , 100 , 40 )
se = st.selectbox("SEX", ['M' , "F"])
chest_pain=st.selectbox("Chest pain type ",["ATA" , "NAP" , "TA" , "ASY"])
resting_bp=st.number_input("resting blood pressure(mm Hg)" , 80 , 200,120)
cholesterol = st.number_input("Cholesterol(mm/dl)" ,100 , 600 ,200)
fasting_bs=st.selectbox("Fasting Blood Sugar > 120mm/dl " , [0,1])
resting_ecg=st.selectbox("Resting ECG" , ["Normal" , "ST" , "LVH"])
max_hr = st.slider("Max Heart Rate" , 60 , 220 , 150)
exercise_angina=st.selectbox("Exercise-Induced Angina" , ["Y","N"])
oldpeak = st.slider("oldpeak(ST Depression)" , 0.0 , 6.0 , 1.0)
st_slope = st.selectbox("ST Slope" , ["Up" , "Flat"  ,"Down"])

# Create a raw input dictionary
raw_input = {
    'Age': age,
    'Sex': se,
    'ChestPainType': chest_pain,
    'RestingBP': resting_bp,
    'Cholesterol': cholesterol,
    'FastingBS': fasting_bs,
    'RestingECG': resting_ecg,
    'MaxHR': max_hr,
    'ExerciseAngina': exercise_angina,
    'Oldpeak': oldpeak,
    'ST_Slope': st_slope
}

#  input dataframe
input_df = pd.DataFrame([raw_input])

# One-hot encode categorical features to match training data
input_df = pd.get_dummies(input_df, columns=['Sex', 'ChestPainType', 'RestingECG', 'ExerciseAngina', 'ST_Slope'], drop_first=False)

# Fill in missing columns 
for col in expected_columns:
    if col not in input_df.columns:
        input_df[col] = 0

# Reorder columns
input_df = input_df[expected_columns]

# Scale the input
scaled_input = scaler.transform(input_df)

# Make prediction
prediction = model.predict(scaled_input)[0]
probability = model.predict_proba(scaled_input)[0]

# Show result
if prediction == 1:
    st.error(f"⚠️ High Risk of Heart Disease (Probability: {probability[1]:.2%})")
    st.info(
    "This prediction is for informational purposes only and is not a substitute for professional medical advice."
)
else:
    st.success(f"✅ Low Risk of Heart Disease (Probability: {probability[0]:.2%})")
    st.info(
    "This prediction is for informational purposes only and is not a substitute for professional medical advice."
)
    
st.caption("Built by Bhavya Aggarwal • Educational Project • Not a Medical Device")

    










